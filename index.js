// Input panjang dan tinggi segitiga dari pengguna
let panjang = parseFloat(prompt("Masukkan panjang segitiga: "));
let tinggi = parseFloat(prompt("Masukkan tinggi segitiga: "));

// Hitung luas segitiga
let luas =  1.2* panjang * tinggi;

// Tampilkan hasil
console.log("Luas segitiga adalah: " + luas);